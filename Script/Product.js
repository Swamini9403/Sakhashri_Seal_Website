/* ================= OPEN FULL PAGE ================= */

function openProductPage(product) {
  document.getElementById("productPage").style.display = "block";
  loadProduct(product);

  document.getElementById("clickSound").currentTime = 0;
  document.getElementById("clickSound").play();
}


/* ================= CLOSE PAGE ================= */

function closeProductPage() {
  document.getElementById("productPage").style.display = "none";
}


/* ================= LOAD PRODUCT ================= */

function loadProduct(product) {
  let data = productData[product];

  document.getElementById("fullTitle").innerText = product;
  document.getElementById("fullDesc").innerHTML = data.desc;
  document.getElementById("fullImage").src = data.img;
}


/* ================= SIDEBAR CLICK ================= */

document.querySelectorAll("#menuList li").forEach(li => {
  li.addEventListener("click", () => {

    document.querySelectorAll("#menuList li").forEach(l => l.classList.remove("active"));
    li.classList.add("active");

    loadProduct(li.innerText);
  });
});


/* ================= FLOATING SHAPES ================= */

document.addEventListener("mousemove", (e) => {
  const shapes = document.querySelectorAll(".floating-shapes span");

  shapes.forEach((shape, index) => {
    let speed = (index + 1) * 0.02;
    let x = (window.innerWidth - e.pageX * speed) / 100;
    let y = (window.innerHeight - e.pageY * speed) / 100;

    shape.style.transform = `translate(${x}px, ${y}px)`;
  });
});


/* ================= FEATURE CARD ================= */

const cards = document.querySelectorAll(".feature-card");

cards.forEach(card => {
  card.addEventListener("click", () => {
    cards.forEach(c => c.classList.remove("active"));
    card.classList.add("active");
  });
});

const toggleBtn = document.getElementById("themeToggle");

// Load saved theme
if (localStorage.getItem("theme") === "dark") {
  document.body.classList.add("dark");
  toggleBtn.innerText = "☀️";
}

// Toggle theme
toggleBtn.addEventListener("click", () => {
  document.body.classList.toggle("dark");

  if (document.body.classList.contains("dark")) {
    localStorage.setItem("theme", "dark");
    toggleBtn.innerText = "☀️";
  } else {
    localStorage.setItem("theme", "light");
    toggleBtn.innerText = "🌙";
  }
});